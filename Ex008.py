m = float(input('Digite um valor em metro para conversão: '))
dm = m*10
cm = (m*10)*10
mm = ((m*10)*10)*10
dam= m/10
hm = (m/10)/10
km = ((m/10)/10)/10
print('Valor digitado: {}m\n'.format(m))
print('Conversão de metro para decímetro')
print('{}m é igual a {}dm\n'.format(m, dm))
print('Conversão de metro para centímetro')
print('{}m é igual a {}cm\n'.format(m, cm))
print('Conversão de metro para milímetro')
print('{}m é igual a {}mm\n'.format(m, mm))
print('Conversão de metro para decametro')
print('{}m é igual a {}dam\n'.format(m, dam))
print('Conversão de metro para hectometro')
print('{}m é igual a {}hm\n'.format(m, hm))
print('Conversão de metro para quilometro')
print('{}m é igual a {}km'.format(m, km))
